package com.tokelon.toktales.android.render.opengl;

public class OpenGLException extends Exception {

	
	private static final long serialVersionUID = 1770819782933813162L;

	public OpenGLException() {
		super();
	}

	public OpenGLException(String detailMessage, Throwable throwable) {
		super(detailMessage, throwable);
	}

	public OpenGLException(String detailMessage) {
		super(detailMessage);
	}

	public OpenGLException(Throwable throwable) {
		super(throwable);
	}

}
