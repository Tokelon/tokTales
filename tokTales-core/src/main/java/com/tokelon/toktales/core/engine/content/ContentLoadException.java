package com.tokelon.toktales.core.engine.content;

public class ContentLoadException extends ContentException {

	
	private static final long serialVersionUID = 1467963960595159099L;

	public ContentLoadException() {
		super();
	}

	public ContentLoadException(String message, Throwable cause) {
		super(message, cause);
	}

	public ContentLoadException(String message) {
		super(message);
	}

	public ContentLoadException(Throwable cause) {
		super(cause);
	}
	
}
