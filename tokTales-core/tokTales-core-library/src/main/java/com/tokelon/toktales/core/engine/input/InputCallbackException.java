package com.tokelon.toktales.core.engine.input;

public class InputCallbackException extends Exception {

	private static final long serialVersionUID = -7585427480746832680L;

	public InputCallbackException() {
		super();
	}

	public InputCallbackException(String message, Throwable cause) {
		super(message, cause);
	}

	public InputCallbackException(String message) {
		super(message);
	}

	public InputCallbackException(Throwable cause) {
		super(cause);
	}

}
