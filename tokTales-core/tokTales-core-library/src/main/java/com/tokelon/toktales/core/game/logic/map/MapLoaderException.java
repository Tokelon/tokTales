package com.tokelon.toktales.core.game.logic.map;

public class MapLoaderException extends Exception {

	
	private static final long serialVersionUID = -1012942045779338672L;

	public MapLoaderException() {
		super();
	}

	public MapLoaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public MapLoaderException(String message) {
		super(message);
	}

	public MapLoaderException(Throwable cause) {
		super(cause);
	}

}
