package com.tokelon.toktales.tools.core.tiled;


public class TiledMapFormatException extends MapFormatException {

	
	private static final long serialVersionUID = -8837129521314880103L;

	public TiledMapFormatException() {
		super();
	}

	public TiledMapFormatException(String message) {
		super(message);
	}

	public TiledMapFormatException(Throwable cause) {
		super(cause);
	}

	public TiledMapFormatException(String message, Throwable cause) {
		super(message, cause);
	}

}
