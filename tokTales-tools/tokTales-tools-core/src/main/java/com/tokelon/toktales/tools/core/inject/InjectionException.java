package com.tokelon.toktales.tools.core.inject;

public class InjectionException extends RuntimeException {


	private static final long serialVersionUID = -7414510608545193474L;

	public InjectionException() {
		super();
	}

	public InjectionException(String arg0) {
		super(arg0);
	}

	public InjectionException(Throwable arg0) {
		super(arg0);
	}

	public InjectionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
