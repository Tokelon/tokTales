package com.tokelon.toktales.core.engine.content;

public class AssetLoadException extends AssetException {


	private static final long serialVersionUID = -3528615822093291442L;

	public AssetLoadException() {
		super();
	}

	public AssetLoadException(String message, Throwable cause) {
		super(message, cause);
	}

	public AssetLoadException(String message) {
		super(message);
	}

	public AssetLoadException(Throwable cause) {
		super(cause);
	}
	
}
